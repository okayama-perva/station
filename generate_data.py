def operator(name):
    global _operator
    _operator = name

def infer_service(name: str) -> str:
    for label in ("各駅停車", "各停", "通勤急行", "快特", "特急", "急行", "快速"):
        if label in name:
            return label
    return "各駅停車"


def infer_base_name(name: str, service: str) -> str:
    if service == "各駅停車":
        return name
    return name.replace(service, "").strip(" ・()（）")


def add(name, stations, loop=False, service=None, base_name=None):
    """stations: [(駅名, 次駅までの分), ...] 最後の駅のtimeはloop時のみ使用"""
    service = service or infer_service(name)
    base_name = base_name or infer_base_name(name, service)
    lines.append({
        "name": name,
        "base_name": base_name,
        "service": service,
        "operator": _operator,
        "stations": stations,
        "loop": loop,
    })

